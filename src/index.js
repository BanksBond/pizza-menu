function Footer() {
  const hour = new Date().getHours();
  console.log(hour);
  const openHour = 12;
  const closedHour = 22;
  const isOpen = hour >= openHour && hour <= closedHour;

  return (
    <footer className="footer">
      {isOpen ? (
        <Order closedHour={closedHour} />
      ) : (
        <div className="order">
          <p>We're closed until {openHour}:00. Come visit us or Order then! </p>
        </div>
      )}
    </footer>
  );
}

function Order({ closedHour }) {
  return (
    <div className="order">
      <p>We're open until {closedHour}:59. Come visit us or Order Now! </p>
      <button className="btn">Order</button>
    </div>
  );
}

